package arrayofobjectsdemo;

/**
 * @author nhasa
 *
 */
public class Account {
	
	private String name;
	private int number;
	
	//...Setter Methods...//
	public void setName(String name)
	{
		this.name=name;
		
	}
	public void setNumber(int number)
	{
		this.number=number;
		
	}
	
	//..Getter Methods...//
	public String getName()
	{
		return this.name;
	}
	public int getNumber()
	{
		return this.number;
	}

}
